$(document).ready(function(){

    $("#Botones g").on("click",seleccionarDept);
    $("#deps").change(selectDepsM);
    depInicialMobile();
});

function seleccionarDept(){

    var isMobile =$(".mobile-ui-mapa").css("display")=="block";
    if (!isMobile){

        $("#Botones g.activo").removeClass("activo");
        $(this).addClass("activo");
    
        var num=$(this).index();
        console.log(num);
        $(".titulo-info").fadeOut(300,function(){
            $(".titulo-info").html(comunidades[num].comunidad);
            $(".titulo-info").fadeIn(300);
        });
    
        $(".departamento-info").fadeOut(300,function(){
            $(".departamento-info").html(comunidades[num].departamento);
            $(".departamento-info").fadeIn(300);
        });
        if(!$(".previo-cont").hasClass("oculto")){
    
            $(".previo-cont").fadeOut(300,function(){
                $(".texto-comunidad").html(comunidades[num].procedimiento);
                $(".comunidad-cont").fadeIn(300);
                $(".previo-cont").addClass("oculto");
    
            });
    
        }
        else{
            $(".comunidad-cont").fadeOut(300,function(){
                $(".texto-comunidad").html(comunidades[num].procedimiento);
                $(".comunidad-cont").fadeIn(300);
               
    
            });
        }

        $(".link-comunidad a").attr("href",comunidades[num].link);
    }

    
}

